-- 12 - Inspect selected data pages with DBCC PAGE
-------------------------------------------------------------------------------

/*
DBCC PAGE with print option 3 shows the page header and row contents
The selected page addresses will also be used later by RESTORE DATABASE ... PAGE
*/

DECLARE
@DataPageFID INT,
@DataPagePID INT,
@SQL NVARCHAR(MAX);

DECLARE PageCursor CURSOR LOCAL FAST_FORWARD FOR
SELECT
file_id,
page_id
FROM tempdb.dbo.#Q0029_PageRestoreTarget
WHERE database_name = 'ExamplesDB_MultiplePageRestore'
ORDER BY target_id;

OPEN PageCursor;

FETCH NEXT FROM PageCursor INTO
@DataPageFID,
@DataPagePID;

WHILE @@FETCH_STATUS = 0
BEGIN
    SET @SQL = N'DBCC PAGE (''ExamplesDB_MultiplePageRestore'', '
             + CAST(@DataPageFID AS NVARCHAR(20))
             + N', '
             + CAST(@DataPagePID AS NVARCHAR(20))
             + N', 3);';

    PRINT(@SQL);
    EXEC (@SQL);

    FETCH NEXT FROM PageCursor INTO
    @DataPageFID,
    @DataPagePID;
END;

CLOSE PageCursor;
DEALLOCATE PageCursor;
GO

